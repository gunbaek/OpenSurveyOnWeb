package MyBean;
public class MyInfo{
	private String id;
	private String passwd;

	public MyInfo(){
		id = "";
		passwd = "";
	}

	public void setId(String id){
		this.id = id;
	}
	public void setPasswd(String passwd){
		this.passwd = passwd;
	}
	public String getId(){
		return id;
	}
	public String getPasswd(){
		return passwd;
	}
}